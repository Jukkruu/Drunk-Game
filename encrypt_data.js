const cipher = (text, key) => {
    if (!text || !key) return text;
    const k = String(key);
    return Array.from(text).map((char, i) =>
        String.fromCharCode(char.charCodeAt(0) ^ k.charCodeAt(i % k.length))
    ).join('');
};

const encode = (text, key) => {
    const scrambled = cipher(text, key);
    return Buffer.from(scrambled).toString('base64');
};

