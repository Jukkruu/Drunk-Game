// --- Card Game Module ---
const Cards = {
    deck: [],
    history: [],

    init() {
        this.reset();
        document.getElementById('draw-btn').addEventListener('click', () => this.draw());
        document.getElementById('shuffle-btn').addEventListener('click', () => this.shuffle());
        document.getElementById('reset-cards-btn').addEventListener('click', () => this.reset());
    },

    createDeck() {
        this.deck = [];
        suits.forEach(suit => {
            values.forEach(value => {
                this.deck.push({
                    value,
                    suit,
                    color: (suit === '♥' || suit === '♦') ? 'card-red' : 'card-black'
                });
            });
        });
    },

    shuffle() {
        for (let i = this.deck.length - 1; i > 0; i--) {
            const j = Math.floor(Math.random() * (i + 1));
            [this.deck[i], this.deck[j]] = [this.deck[j], this.deck[i]];
        }
        if (this.deck.length > 0) Toast.show("สลับสำรับเรียบร้อย!");
    },

    draw() {
        if (this.deck.length === 0) {
            Toast.show("ไพ่หมดสำรับแล้ว! กรุณารีเซ็ต");
            return;
        }
        const card = this.deck.pop();
        this.history.unshift(card);
        this.updateUI(card);
    },

    reset() {
        this.createDeck();
        this.shuffle();
        this.history = [];
        document.getElementById('card-val').innerText = '?';
        document.getElementById('card-suit').innerText = '🃏';
        document.getElementById('card-card').className = 'card-display';
        // Toast.show("เริ่มใหม่สำเร็จ!");
        this.updateHistoryUI();
    },

    updateUI(card) {
        document.getElementById('card-val').innerText = card.value;
        document.getElementById('card-suit').innerText = card.suit;
        document.getElementById('card-card').className = `card-display ${card.color}`;
        this.updateHistoryUI();
    },

    updateHistoryUI() {
        const list = document.getElementById('history-list');
        list.innerHTML = '';
        this.history.forEach((card, index) => {
            const item = document.createElement('div');
            item.className = 'history-item';

            const label = document.createElement('span');
            label.className = card.color;
            label.style.fontWeight = '700';
            label.innerText = `${card.value} ${card.suit}`;

            const id = document.createElement('span');
            id.className = 'history-index';
            id.innerText = this.history.length - index;

            item.appendChild(label);
            item.appendChild(id);
            list.appendChild(item);
        });
    }
};

// --- Crocodile Dentist Module ---
const Croc = {
    trapTooth: -1,
    teethCount: 12,

    init() {
        document.getElementById('reset-croc-btn').addEventListener('click', () => this.reset());
        this.reset();
    },

    reset() {
        this.trapTooth = Math.floor(Math.random() * this.teethCount);
        const container = document.getElementById('teeth-container');
        const resetBtn = document.getElementById('reset-croc-btn');

        container.innerHTML = '';
        document.body.classList.remove('flash-red');
        resetBtn.classList.add('hidden');

        for (let i = 0; i < this.teethCount; i++) {
            const tooth = document.createElement('div');
            tooth.className = 'tooth';
            tooth.addEventListener('click', (e) => this.handlePress(i, e.target));
            container.appendChild(tooth);
        }
    },

    handlePress(index, el) {
        if (index === this.trapTooth) {
            el.classList.add('trap');
            document.body.classList.add('flash-red');
            document.getElementById('reset-croc-btn').classList.remove('hidden');
            document.querySelectorAll('.tooth').forEach(t => t.classList.add('pressed'));
            Toast.show("มึงโดน!", "red");
        } else {
            el.classList.add('pressed');
        }
    }
};

// --- Deep Talk Module ---
const DeepTalk = {
    currentCat: 'love',
    pools: {},

    init() {
        this.resetPools();
        this.initChips();
        document.getElementById('next-q-btn').addEventListener('click', () => this.next());
        document.getElementById('reset-q-btn').addEventListener('click', () => this.resetPools());
    },

    resetPools() {
        Object.keys(ques).forEach(cat => {
            this.pools[cat] = [...ques[cat]];
        });
        document.getElementById('question-text').innerText = 'รีเซ็ตคำถามเรียบร้อย! เลือกหมวดหมู่แล้วกด "ถัดไป"';
        Toast.show("รีเซ็ตคำถามทั้งหมดแล้ว", "pink");
    },

    initChips() {
        const chips = document.querySelectorAll('.chip');
        const label = document.getElementById('q-category-label');
        const text = document.getElementById('question-text');

        chips.forEach(chip => {
            chip.addEventListener('click', () => {
                chips.forEach(c => c.classList.remove('active'));
                chip.classList.add('active');
                this.currentCat = chip.dataset.cat;
                label.innerText = `หมวดหมู่: ${chip.innerText}`;
                text.innerHTML = `เปลี่ยนเป็นหมวด <b>${chip.innerText}</b> แล้ว! <br> กดปุ่มด้านล่างเพื่อเริ่ม`;
            });
        });
    },

    next() {
        const pool = this.pools[this.currentCat];
        if (pool.length === 0) {
            document.getElementById('question-text').innerText = 'หมดคลังคำถามหมวดนี้แล้ว! กำลังรีเซ็ต...';
            this.pools[this.currentCat] = [...ques[this.currentCat]];
            return;
        }
        const index = Math.floor(Math.random() * pool.length);
        const question = pool.splice(index, 1)[0];
        document.getElementById('question-text').innerText = question;
    }
};

