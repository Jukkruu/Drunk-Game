                navBtns.forEach(b => b.classList.remove('active'));
                btn.classList.add('active');

                sections.forEach(s => {
                    s.classList.remove('active');
                    s.style.display = 'none';
                });

                const target = document.getElementById(game);
                target.style.display = '';
